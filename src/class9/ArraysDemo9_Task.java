package class9;

/*
write a program that will print I Love Java
 */
public class ArraysDemo9_Task {
    public static void main(String[] args) {
        char[] letters = {'I', ' ', 'L', 'o', 'v', ' ', 'J', 'a', 'v', 'a'};
        for (int i = 0; i < letters.length; i++) {
            System.out.print(letters[i]);
        }
    }
}