package class7Wednesday01112023.class7HomeWork;

//Print even numbers from 20 to 1 (2 ways) (3nd For Loop)
public class ForLoopEven20to1_3rd {
    public static void main(String[] args) {
        for (int i = 20; i > 1; i--) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
